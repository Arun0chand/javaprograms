package testnglist;

public class SwapNumbers {
	
	
	public static void main(String[] args) {
		
		
		int n1=2;
		int n2=3;
		
		System.out.println("Before swapping");
		System.out.println("n1=>"+n1);
		System.out.println("n2=>"+n2);
		
		int temp;
		
		temp=n1;//2
		n1=n2;//3
		n2=temp;//2
		System.out.println("After swapping");
		System.out.println("n1=>"+n1);
		System.out.println("n2=>"+n2);
		
	}

}
