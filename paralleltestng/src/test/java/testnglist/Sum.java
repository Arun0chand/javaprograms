package testnglist;

public class Sum {
 public static void main(String[] args) {
	
	int n1=2;
	int n2=3;
	System.out.println("before sorting");
	System.out.println("n1=>"+n1);
	System.out.println("n2=>"+n2);
	
	n1=n1*n2;//6
	n2=n1/n2;//3
	n1=n1/n2;//2
	System.out.println("After sorting");
	System.out.println("n1=>"+n1);
	System.out.println("n2=>"+n2);
}
}
